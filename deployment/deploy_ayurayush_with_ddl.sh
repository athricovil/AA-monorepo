echo "ðŸ” JWT secret & expiration passed via JVM -D options"
echo "ðŸ˜ PostgreSQL: $DB_NAME | User: $DB_USER | Pass: $DB_PASS"
echo "ðŸ“¦ Backend: sudo systemctl status aabackend"
echo "ðŸ“Œ Ensure Azure NSG allows port 5432 for IP: $MY_IP"

